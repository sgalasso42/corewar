#include "../inc/corewar.h"

int		ft_isnumber(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((!ft_isdigit(str[i])) && !(i == 0 && str[i] == '-'))
			return (0);
		i++;
	}
	return (1);
}
